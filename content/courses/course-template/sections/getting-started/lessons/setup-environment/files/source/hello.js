// A simple Hello World program to test your JavaScript setup

console.log("Hello, World!");

// You can also try more complex examples:

// 1. Using variables
const greeting = "Hello";
const name = "Developer";
console.log(`${greeting}, ${name}!`);

// 2. Using a function
function greet(person) {
  return `Hello, ${person}!`;
}

console.log(greet("JavaScript Learner"));

// 3. Using an arrow function with template literals
const greetArrow = (person) => `Greetings, ${person}!`;
console.log(greetArrow("Coding Enthusiast"));
